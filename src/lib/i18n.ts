export type TranslationKey = keyof typeof translations.en;

const interpolate = (value: string, params?: Params): string => {
  if (!params) {
    return value;
  }

  return Object.entries(params).reduce(
    (text, [key, replacement]) => text.replaceAll(`{${key}}`, String(replacement)),
    value
  );
};

export const translate = (language: Language, key: TranslationKey, params?: Params): string =>
  interpolate(translations[language][key] ?? translations.en[key], params);

export const useTranslation = () => {
  const language = useLanguageStore((state) => state.language);
  const setLanguage = useLanguageStore((state) => state.setLanguage);

  const t = useCallback(
    (key: TranslationKey, params?: Params) => translate(language, key, params),
    [language]
  );

  return { language, setLanguage, t };
};
